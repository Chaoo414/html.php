<body>
<?php echo "Hello world!"; ?>: 出 "Hello world!" 字串。echo 用來輸出內容。
<h1>html</h1>: 用來定義一個最重要的標題
<?php echo "Hello world!"; ?>:用來輸出 "Hello world!" 字串。
<h1>html</h1>: 用來定義一個最重要的標題
<?php echo "Hello world!"; ?>: 用來輸出 "Hello world!" 字串。
</body>: HTML 主要內容區塊的結束標籤。
</html>: HTML 文件的結束標籤。
