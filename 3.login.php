$_POST["id"]: 表示從 POST 方法提交的表單中獲取名為 "id" 的欄位的值。
$_POST["pwd"]: 表示從 POST 方法提交的表單中獲取名為 "pwd" 的欄位的值。
==: 是一個比較運算符，用於比較兩個值是否相等。
&&: 是一個邏輯運算符，表示“且”的意思，兩個條件都必須成立才會執行後續的程式碼。
